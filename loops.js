// Use a while loop to print “I am a happy programmer” 10 times
let i = 1;  
while (i <= 10) {
  console.log('I am a happy programmer');
  i++;
}

// Use a for loop to log all numbers from 1 to 20 
for (i=1; i <= 20; i++) {
  console.log(i)
}

// Use a for loop to loop through all numbers from 50 to 1 backwards and display their sum 
let sum = 0 
for (let num = 50; num >= 1; num-- ) {
  sum = sum + num; 
}
console.log(sum)

